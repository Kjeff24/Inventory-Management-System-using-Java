    // Displays vendors and other details in JTable
    public void DisplayVendor()
    {
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InventoryDB", "root", "root");
            St = Con.createStatement();
            Rs = St.executeQuery("select prodvend as 'Vendors', prodname as 'Prod Name', prodid as 'Prod Id', prodcat as 'Category', prodqty as 'Quantity', prodbuyp as 'Buying Price', prodgrossp as 'Gross Price' from producttb");
            VendorTable.setModel(DbUtils.resultSetToTableModel(Rs));
            
        }catch(SQLException e)
        {
            e.printStackTrace();
        }
    }

