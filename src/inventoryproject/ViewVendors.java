    // Delete row in the database by entering the product id
    private void deletebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deletebtnMouseClicked

        if(txtprodid.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "You need to enter an integer!");
        }
        else
        {
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/InventoryDB", "root", "root");
                String Id = txtprodid.getText();
                String Query = "Delete from inventorydb.producttb where prodid="+Id;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query);
                DisplayVendor();
                txtprodid.setText("");
                JOptionPane.showMessageDialog(null, "Vendor has been successfully deleted!");
            }catch(SQLException | NumberFormatException e)
            {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_deletebtnMouseClicked

    private void searchfieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchfieldKeyReleased
        DefaultTableModel model = (DefaultTableModel) VendorTable.getModel();
        String search = searchfield.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter(model);
        VendorTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_searchfieldKeyReleased

